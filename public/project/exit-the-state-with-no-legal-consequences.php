                    <h2>Qatar grants 3-month amnesty period for illegal residents to exit the state with no legal consequences.</h2>
                    
                    <p>â€‹As of 1st September 2016, the Ministry of Interior in Qatar had announced the start of a grace period for residents living illegally in Qatar.</p>
<p>
This is the first grace period announcement after almost a decade.

Under this grace period, those living or working illegally in Qatar are given a 3- month period to leave the country without any legal consequences, until the 1st of December, 2016.</p>
<p>
As the new immigration laws will come in to force by 14th December 2016, the government is allowing expats remaining illegally in the State of Qatar to rectify their actions by leaving the State without being held accountable. As part of the new changes, the government is adamant to run the new <a href="index.php">immigration laws</a> smoothly, and giving this grace period allows them to start the new process. </p>
<p>
Many of those who have been illegally residing in the State of Qatar have lined up and taken the offer of departing without any legal consequences. It was reported that some laborers were living illegally in Qatar, since fleeing their workplace, after not being paid wages for their services. Consequently, the new law changes prohibit this from happening in the State of Qatar, with the introduction of the Wage Protection System in labor laws coming into force in less than a month.
</p>
                    
 <p>As per instructions given by the Ministry of Interior, all illegal residents will need to visit the Search and Follow department to finalize their exit. Their documents needed to finalize this process are: </p>                   
                    
                    <ul>
                       <li> A valid passport or travel document. </li>
                       <li>An open ticket or a ticked reserved after 3 working days </li>
                       <li>ID card or copy of entry visa </li>
                    </ul>
                    
       <p>The Search and Follow department will be operating from the 1st of September 2016 to the 1st of December 2016, from Sunday to Thursday, 2 pm - 8 pm.</p>
<p>
This period provided by the ministry is proof of effective change in the State of Qatar, specifically with the changes in immigration and <a href="index.php">labor law.</a> The developments in the law have enhanced the strength in the state policies for international human rights and success in implementing the Qatar National Vision 2030. This amnesty period allows the changes to soon take effect successfully with no legal hardships and minimal delay in process.</p>   
                    
