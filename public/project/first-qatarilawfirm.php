                    <h4>‘Why is Corporate Responsibility important to <strong> Al Sulaiti Law Firm? </strong>’</h4>     
                    
                    
                    
       
<p>
Whilst many may remain confused about our recent pledge to the United Nations Global Compact (UNGC), being the first Qatari Law Firm to do so, we felt the need for an explanation on our pledge to the UNGC’s ten principles that promote corporate sustainability. All organizations around the world are accountable to run a responsible and sustainable business for the benefit of our organizations, people, community and the world as a whole. A law firm is a <a href="http://alsulaitilawfirm.com/service.php">legal service provider,</a> and to provide the best services, we must have a clear understanding of the clients’ needs, the community we live in and our pledge and respect for the law. In the same way in which the law and our community have been a great benefits to us, we must give back to these core pillars of society by making it our responsibility to be more involved in the promotion of worthy change through our corporate responsibility policy.</p>
