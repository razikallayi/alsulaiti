                    <h2>Valid Litigation Proceedings: A Pressing Necessity for Restitution of Rights </h2>
                    
                    <p>Prevention is better than cure. This principle squarely applies to today's realities of the legal system including gross faults in corporate litigation before the courts. This results in rendering numerous rulings entailing lapse of rights because claimantsâ€™ litigation proceedings against corporates eventually become tossed as legally inadmissible. </p>
<p>In this article, we will tackle the damages inflicted upon the claimants when failing to follow valid litigation proceedings. </p>
<p>
We will also give advices and directions to be followed so that the litigation against existing and registered companies is grounded upon valid proceedings from the onset of litigation and until the rendering of the verdict. </p>
<p>
This article also tackles valid litigation proceedings as a function of the opponent company as follows:
</p>
<ul>
 <li>Claimants institute lawsuits against sole proprietorship firms but not against the proprietor only to be eventually surprised that the case is tossed because of failure to include the name of the owner of the firm. Other claimants are shocked when their cases are dismissed due to proceeding against one of the branches of the company without adding the registered office as a litigant in the case. </li>
</ul>
<p>There is no doubt that such flaws or faults committed by the claimant result in grave damages sustained thereby including without being limited to the following:</p>        
                    
                    
                    <ul class="responsive-accordion responsive-accordion-default bm-larger margine-bottom-20">
                        <li>
                            <div class="responsive-accordion-head">First <i class="fa fa-plus responsive-accordion-plus icon-fw"></i><i class="fa fa-minus responsive-accordion-minus icon-fw"></i></div>
                            <div class="responsive-accordion-panel">
                            <p>Enormous consumption of time from onset of litigation until adjudication for two or three years with considerable efforts gone in vain. As a matter of fact, the damages may be graver when the rights lapse due to the prescription of time. </p>
                            </div>
                        </li>
                        
                        <li>
                            <div class="responsive-accordion-head">Second<i class="fa fa-plus responsive-accordion-plus icon-fw"></i><i class="fa fa-minus responsive-accordion-minus icon-fw"></i></div>
                            <div class="responsive-accordion-panel">
                            <p>The lapse of some rights due to the prescription of time, for example labor rights which are subject to prescription of time after one year from termination of the services, as well as rights arising from lease contracts and pension which are subject to prescription of time for three years. Whereas these prescription terms may lapse without valid litigation against companies, therefore, rights are completely lost due to the lapse of the term during which the claimant can legally proceed against the company, as supposed to be initially, for such rights. </p>
                            </div>
                        </li>
                        
                        <li>
                            <div class="responsive-accordion-head">Third<i class="fa fa-plus responsive-accordion-plus icon-fw"></i><i class="fa fa-minus responsive-accordion-minus icon-fw"></i></div>
                            <div class="responsive-accordion-panel">
                            <p>Claimants are caught by surprise when their opponents do not show up before court, then a ruling is rendered in their favor for the amounts claimed thereby, and upon execution, they realize that the ruling is rendered against a company branch and not the head office, and whereas the branch, and as established by the law, is not a body corporate, therefore, the branch has a separate financial entity so that it may be seized against the settlement, therefore, rights lapse and claimants must proceed against the main office of that branch. </p>
                            </div>
                        </li>
                        
                        <li>
                            <div class="responsive-accordion-head">Fourth<i class="fa fa-plus responsive-accordion-plus icon-fw"></i><i class="fa fa-minus responsive-accordion-minus icon-fw"></i></div>
                            <div class="responsive-accordion-panel">
                            <p>Companies incur substantial amounts to vainly restitute their rights. </p>
                            <p>This article deals with the valid legal proceedings against duly established and existing companies from the beginning and we will attempt as much as possible to do so as follows:</p>
                            <p class="he">First</p>
                            <p><strong>Litigation against Limited Liability Companies,</strong> for instance, Saada Trading and Contracting W.L.L. The lawsuit must be instituted in such name. </p>
                            <p class="he">Second</p>
                            <p><strong>Litigation against sole proprietorship companies,</strong> for instance, Saada Electrical Works Company, owned by Mr. XZ, therefore the litigation must be in such name and adding the name of the owner as an opponent. "Saada Electrical Works Establishment whose proprietor is Mr. XZ. </p>
                            <p class="he">Third</p>
                            <p><strong>Litigation against company branches subsidiary of the main office of the parent company.</strong> For example, Saada Trading and Contracting Company, Commercial Registration No. 1/5856, which is a clear reference that the company is a branch of the company with Commercial Registration No. 5856, which is the valid legal opponent in its capacity of being the head office for the branch 1/5856. Therefore litigation proceeding shall be as follows: Mostaqbal Trading and Contracting, the head office for the branch Saada Trading and Contracting Co. </p>
                            <p class="he">Fourth</p>
                            <p><strong>Litigation against shareholding Companies:</strong> It is a matter of law that litigation against such companies is originally against the managing director of the company and that when the managing director and the chairman of the board are the same personality, then, proceedings ought to be directed to both the managing director and the chairman of the board jointly. </p>
                            <p class="he">Fifth</p>
                            <p><strong>Litigation against foreign companies:</strong> In this case, litigation is against the branch in operation in the State of Qatar by service of a notice vide the branch or the agent in the State of Qatar. Where the branch has no offices in the State of Qatar, then the main office is subject to proceedings in the foreign state via the diplomatic channels.</p>

                            </div>
                        </li>
                        
                    </ul>
                    
                    <p class="italic">Where the above legal procedures are validly followed while litigating against companies, it saves a substantial amount of time towards restitution. </p>   
                    
                    <h4>Recommendations for consideration by the Claimants:</h4>
       
<p>
Prior to the institution of the case, procure to issue a<strong> (valid and updated)</strong> copy of the Commercial Registration of the company, and to verify valid litigation proceedings from the onset of litigation. The issuance of an updated copy of the commercial record is that some companies transform from limited liability companies to sole proprietorship companies and vice versa. Also, companies which are independent from the onset of the contractual relations with the claimant then transformed when the lawsuit is instituted to a branch subsidiary to another company, therefore, for the need to verify this point, a recent and updated extract of the commercial registration is of dire necessity to determine the status of the company, particularly with regard to the time of instituting the legal proceedings. </p>
