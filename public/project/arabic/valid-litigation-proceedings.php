                    <p>-استدعاءً لمبدأ الوقاية خيراً من العلاج، أكتب هذا المقال نظراً لما نلمسه يومياً في الواقع العملي من أخطاءٍ جسيمةٍ في اختصام الشركات بعضها البعض أمام ساحات القضاء، وما يترتب على ذلك من صدور أحكامٍ عديدةٍ بضياع الحقوق، نظراً لقيام أصحابها باختصام الشركات بشكلٍ غير قانونيٍ. </p>
                    
                    <p>-وحيث إننا في هذا المقال؛ سنتناول الأضرار التي تقع على أصحاب الحقوق عندما يقومون باختصام الشركات بشكلٍ غير قانونيٍ، وسنتناول كذلك النصائح والإرشادات الواجب اتباعها كي يكون اختصام الشركات قائمةً بشكلٍ سليم منذ بداية النزاع وحتى صدور حكمٍ في القضية. كما سنتناول كيف يكون الاختصام بشكلٍ سليم حسب نوع الشركة التي تُختصم، وذلك وفقاً للتفصيل التالي:</p>

					<p>-يقوم بعض أصحاب الحقوق بإقامة دعوى ضد إحدى المؤسسات الفردية دون اختصام مالكها، وبعد ذلك يُفاجئ هؤلاء بأن دعواهم غير مقبولة نظراً لعدم إضافة اسم مالك المؤسسة عند الاختصام، وقد يُفاجئ البعض الآخر بعدم قبول قضاياهم، نظراً لإقامتها ضد إحدى فروع الشركات دون إضافة المركز الرئيسي في خانة الاختصام.</p>
                    
                    <p>-ومما لا شك فيه أن في الخطأ السالف الذي يقع فيه أصحاب الحقوق، قد يُصيبهم بأضرارٍ جسيمةٍ تتمثل في:</p>
                    
                    <p>أولاً/ ضياع وقت كبير منذ بداية الاختصام وحتى صدور حكم في القضية، وهو الأمر الذي قد يصل في بعض الأحيان إلى عامين وثلاثة أعوام، وذلك دون أي فائدةٍ تُذكر، بل إن الضرر يكون أعظم إذا ما تعلقت الحقوق بمدة خاصة للتقادم.</p>
                    
                    <p>ثانياً/ ضياع بعض الحقوق بالتقادم المسقط للحقوق، وذلك كما هو الأمر في الحقوق العمالية التي يتقادم الحق الناشئ عنها بمرور عام منذ انتهاء خدمة العامل لدى صاحب العمل، وكذلك تقادم الحقوق الناشئة عن عقود الإيجار ومعاشات التقاعد بمرور خمسة أعوام. وحيث أن مرور هذه المدد دون اختصام للشركات بشكلٍ سليم سيؤدي إلى ضياع الحق بشكلٍ كامل نظراً لمرور المدة التي يجب خلالها مطالبة الشركة- الواجب اختصامها منذ البداية- بهذه الحقوق. </p>
                    
                    <p>ثالثاً/ قد يُفاجئ بعض أصحاب الحقوق بعدم حضور خصومهم جلسات القضية أمام المحكمة، ثم يحصلون على أحكامٍ لصالحهم بالمبالغ التي يُطالبون بها، ثم يقومون بالتنفيذ بعد ذلك ليُفاجئوا بأنهم قد حصلوا على حكمٍ غير قابل للتطبيق من الناحية القانونية، وذلك نظراً-على سبيل المثال-لأن هذا الحكم قد صدر ضد فرع شركةً ما دون اختصام مركزها الرئيسي، وبما أن الفرع-طبقاً للقانون-ليس لديه شخصية اعتبارية؛ فبالتالي فإنه لا يتمتع بذمة مالية مستقلة من الممكن الحجز عليها كي يقوم بالسداد، ونتيجة لذلك يضيع الحق على أصحاب الحقوق ولا يبقى أمامهم سوى اختصاهم للمركز الرئيسي لهذا الفرع من جديد.</p>
                    
