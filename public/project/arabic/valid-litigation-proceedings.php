                    <p>أولاً:-اختصام الشركات ذات المسؤولية المحدودة، وليكن على سبيل المثال شركة تحمل اسم/ السعادة للتجارة والمقاولات ذ.م.م؛ فإن اختصامها هنا يكون بذات الاسم، شركة/ السعادة للتجارة والمقاولات ذ.م.م.</p>
                    
