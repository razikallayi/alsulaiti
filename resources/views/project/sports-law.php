                    <h2>Sports Law (Road to World Cup 2022)</h2>
                    
                    <p>​The 22nd Congress of the International Association of Sports Law (IASL) was held on 17, 18 and 19 of this month in Sochi, Russia. The Russian International Olympic University was the organizer of this event in collaboration with IASL and Al Sulaiti Law Firm as an organizational partner. Mr. Mubarak bin Abdullah Al Sulaiti attended the Congress, representing the firm himself together with his team of lawyers specialized in sports law and  hundreds of sports lawmen professionals from all over the world . Among the attendees were Mr. Lev Belousov, President of the Russian International Olympic University; Mr. Dimitrios P.Panagiotopoulos, President of the International Association of Sports Law and Mr. Janez Kocijancic, Chairman of the European Olympic Committee. In addition, the attendees also included key figures from all over the world who had their active inputs in the presentation of matters related to hosting a World Cup, such as maintaining safety, and security during the World Cup and the importance of legal aspects related to hosting the World Cup.</p>
                    <p>
It is worth noting that the discussions held during all the events of the Congress were very productive as the attendees discussed many matters related to hosting large-scale events and their legal aspects.</p>
<p>
The discussions also included sports security and the mechanisms required to combat hooliganism through the enforcement of legal and procedural means. The Congress reviewed the legal and financial background of large-scale events and the matters related to international tax problems on the sports activities.</p>
<p>
Mr. Klaus Vieweg, Director of the Institute of Law and Technology in Germany, discussed the legal problems facing countries organizing large-scale events.</p>
<p>
In reference to the same context, Mr. Anatoly Peskov, Head of RIOU Security Department, emphasized in his speech on the Russian support of the Qatari situation on the ban of alcoholic beverages during the World Cup 2022 for matters of safety and security. Additionally, the same was asserted by Mr. Mubarak Al Sulaiti who stressed on the security necessities, before starting his speech on ‘The importance of sports law and its effect on the local laws’. </p>
<p>
Mr. Mubarak Al Sulaiti highlighted the efforts made by the State of Qatar for amendment of its laws as much as possible to be more consistent with the international requirements issued by FIFA.</p><p>
In the same context, Mr. Mubarak Al Sulaiti focused in several instances during his speech on human rights legislations and intellectual property protection. Additionally, Mr. Mubarak Al Sulaiti discussed the latest developments concerning the enactment of the recent law on regulating the entry, exit, and expatriates residency, which will be effective on 13/12/2016 and also discussed the intellectual property law No. 17 of 2011, and how this law promotes innovation.</p>
<p>
On the sidelines of the Congress, Mr. Mubarak Al Sulaiti met with many of the aforecited participants and discussed how the Qatari side benefited from the topics that were addressed by the attendees. Also, they discussed how Qatar can maximize the benefit of hosting World Cup 2022 in Qatar.
</p>        
                    
                    <h4>The recommendations of the Congress were as follows:</h4>
                    <ul>
                      <li>To foster cooperation among all organizations and institutions that exercise the sports law throughout the world, by organizing many educational conferences on periodic and intensive basis during the coming period, to establish a legal basis for regulation of procedures related to great events.</li>
                      <li>To persuade FIFA that some matters required by FIFA conflicts with the rules of safety and security observed in countries organizing large-scale events.</li>
                      <li>The possibility of drafting a special law in respect to the countries organizing large-scale sports events.</li>
                    </ul>   
                    
                    
                    
<p>
It is noteworthy that the participation in such Congresses is a great opportunity for the State of Qatar to gain experience, information and ideas about organizing large-scale sports events and the football World Cup. Particularly as these experiences, information and ideas were discussed and presented by many Russian, German and other experts whose countries had the opportunity to organize previous as well as upcoming editions of the football World Cup.</p>
<p>
In the light of the above, Qatar’s participation in such Congresses will increase its organizational weight with respect to organizing large-scale sports events by identifying the mechanisms applied in many countries in terms of organizing similar large-scale sports events.</p>
<p>
In conclusion, attending the 22nd Congress of the International Association of Sports Law in Russia is only a start towards hosting the FIFA World Cup 2022 in Qatar.



   
</p>
                    
